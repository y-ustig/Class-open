package com.probing.Exercise3;

public class SmartDevice {

    int peso;
    String model;
    int price;
    String color;
    boolean waterproof;

public SmartDevice(){

}

public SmartDevice(int peso, String model, int price, String color, boolean waterproof){
    this.peso = peso;
    this.model = model;
    this.price = price;
    this.color = color;
    this.waterproof=waterproof;
}
}

